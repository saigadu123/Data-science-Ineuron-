def append(l,ele):
    return l.append(ele)
    
def clear(l):
    l = []
    return l 
    
def copy(l):
    return l

def count(l,ele):
    return l.count(ele)

def extend(l,li2):
    return l.extend(li2)

def index(l,ele):
    return l.index(ele)

def insert(l,pos,ele):
    return l.insert(pos,ele)

def pop(l):
    return l.pop()

def remove(l,ele):
    return l.remove(ele)

def reverse(l):
    return l.reverse()

def sort(l):
    return l.sort()    
    