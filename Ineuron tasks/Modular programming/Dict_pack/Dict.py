def clear(d):
    return d.clear()
    
def copy(d):
    return d.copy()
    
def fromkeys(d,keys,value):
    return d.fromkeys(keys,value)
    
def get(d,key):
    return d.get(key)
    
def items(d):
    return d.items()
    
def keys(d):
    return d.keys()
    
def pop(d,key):
    return d.pop(key)
    
def popitem(d):
    return d.popitem()
    
def setdefault(d,key):
    return d.setdefault(key)
    
def update(d,item):
    return d.update(item)
    
def values(d):
    return d.values()
    
